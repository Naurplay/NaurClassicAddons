local charset = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789_.~-"
local zeroIndexInSet, charsetLength = {}, strlen(charset)
for i = 1, charsetLength do
    local char = strsub(charset, i, i)
    zeroIndexInSet[char] = i - 1
end

local charsetComplementSet = "[^"..charset.."]"
ts.BoboTalents = {}
function ts.BoboTalents.GetTalents(talentString)
    local _, endIndex = strfind(talentString, "c[lass]?=")
    if (endIndex) then
        local _, playerClass = UnitClass("player")
        playerClass = strlower(playerClass)
        local endOfClass = strfind(talentString, "&", endIndex)
        local class = strlower(strsub(talentString, endIndex+1, endOfClass-1))
        if (class ~= playerClass) then
            print(format(ts.L.WRONG_CLASS, class))
            return
        end
    end
    _, endIndex = strfind(talentString, "t[alents]?=")
    if (endIndex) then
        talentString = strsub(talentString, endIndex + 1)
        -- check for and strip any additional characters not in charset
        local startIndex = strfind(talentString, charsetComplementSet)
        if (startIndex) then
            talentString = strsub(talentString, 1, startIndex)
        end
    end
    local talents = {}
    local talentStringLength = strlen(talentString)
    local level = 10
    for i = 1, talentStringLength, 2 do
        local encodedId = strsub(talentString, i, i + 1)
        if (strlen(encodedId) == 2) then
            local firstChar = strsub(encodedId, 1, 1)
            local secondChar = strsub(encodedId, 2, 2)
            local decodedId = zeroIndexInSet[firstChar] * charsetLength + zeroIndexInSet[secondChar]

            local talentInfo = talentMap[decodedId]
            -- if (not talentInfo) then
            -- -- alert decodedId not found
            -- end
            tinsert(
                talents,
                {
                    tab = talentInfo.t,
                    index = talentInfo.i,
                    rank = talentInfo.r or 1,
                    level = level,
                    id = decodedId
                }
            )
            level = level + 1
        end
    end
    return talents
end
